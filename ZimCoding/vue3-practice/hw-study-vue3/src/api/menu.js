export function getUpperMenu() {
  let resultList = []

  menuList
    .filter((menu) => menu.upper_menu_cd == null)
    .forEach((m) => {
      recursiveMenuList(m, resultList)
    })

  return resultList
}

function recursiveMenuList(parent, seed) {
  let children = menuList.filter((mm) => mm.upper_menu_cd == parent.menu_cd)
  seed.push(parent)
  if (children != null && children.length > 0) {
    let results = []
    children.forEach((m) => recursiveMenuList(m, results))
    parent.children = results
  }
}

