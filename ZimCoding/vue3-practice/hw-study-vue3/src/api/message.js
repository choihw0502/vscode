const rarg = /\{(\d+)\}/g

export function getMessages(code /* ...args */) {
  const textObj = msgList.find((msg) => msg.msg_cd === code)

  if (textObj) {
    let text = textObj.message
    var args = Array.prototype.slice.call(arguments, 1)
    return text.indexOf('{') >= 0
      ? text.replace(rarg, function (str, index) {
          return args[index] || str
        })
      : text
  }
  return code
}
